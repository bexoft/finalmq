static inline bool is_base64(unsigned char c) {
  return (isalnum(c) || (c == '+') || (c == '/'));
}



void Base64::encode(const std::vector<char>& src, std::string& dest)
{
    encode(src.data(), src.size(), dest);
}

void Base64::encode(const char* src, ssize_t len, std::string& dest)
{
  ssize_t ixDest = 0;
  ssize_t i = 0;
  ssize_t j = 0;
  unsigned char char_array_3[3];
  unsigned char char_array_4[4] = {0};

  dest.resize(encodeSize(len));

  while (len--) {
    char_array_3[i++] = *(src++);
    if (i == 3) {
      char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
      char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
      char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
      char_array_4[3] = char_array_3[2] & 0x3f;

      for(i = 0; (i <4) ; i++)
        dest[ixDest++] = base64_chars[char_array_4[i]];
      i = 0;
    }
  }

  if (i)
  {
    for(j = i; j < 3; j++)
      char_array_3[j] = '\0';

    char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
    char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
    char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
    char_array_4[3] = char_array_3[2] & 0x3f;

    for (j = 0; (j < i + 1); j++)
      dest[ixDest++] = base64_chars[char_array_4[j]];

    while((i++ < 3))
      dest[ixDest++] = '=';
  }
  assert(ixDest < dest.size());
  dest.resize(ixDest);
}

ssize_t Base64::decode(const std::string& src, std::vector<char>& dest)
{
    return decode(src.data(), src.size(), dest);
}

ssize_t Base64::decode(const char* src, ssize_t len, std::vector<char>& dest)
{
  ssize_t ixDest = 0;
  dest.resize(decodeSize(len));
  ssize_t i = 0;
  ssize_t j = 0;
  ssize_t in_ = 0;
  unsigned char char_array_4[4];
  unsigned char char_array_3[3] = {0};

  while (len-- && ( src[in_] != '=') && is_base64(src[in_])) {
    char_array_4[i++] = src[in_]; in_++;
    if (i ==4) {
      for (i = 0; i <4; i++)
        char_array_4[i] = tab[char_array_4[i]];

      char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
      char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
      char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

      for (i = 0; (i < 3); i++)
        dest[ixDest++] = char_array_3[i];
      i = 0;
    }
  }

  if (i) {
    for (j = i; j <4; j++)
      char_array_4[j] = 0;

    for (j = 0; j <4; j++)
      char_array_4[j] = tab[char_array_4[j]];

    char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
    char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
    char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

    for (j = 0; (j < i - 1); j++) dest[ixDest++] = char_array_3[j];
  }

  assert(ixDest < dest.size());
  dest.resize(ixDest);

  return ixDest;
}

